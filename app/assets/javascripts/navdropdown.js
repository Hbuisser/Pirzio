  function showSilhouettes() {
    var silhouettes = document.querySelector(".works-silhouettes");
    var signes = document.querySelector(".works-signes");
    var plumes = document.querySelector(".works-plumes");
    signes.style.display = "none";
    plumes.style.display = "none";
    silhouettes.style.display = "block";
  };
